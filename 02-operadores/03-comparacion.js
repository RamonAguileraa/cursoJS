let a = 10;
// operadores relacionales
console.log(a > 5); // mayor
console.log(a >= 5); //mayor igual que
console.log(a < 5); // menor
console.log(a <= 5); // menor igual que

// operadores de igualdad
console.log(a == 10); // evalua si la variable es exactamente igual / compara
console.log(a != 10); // pregunta si es distinto a / evalua si son diferentes
console.log(a === '10'); // true pq el == pregunta si son iguales los valores, no los tipos, ahora === tambien pregunta por el tipo de dato
console.log(a !== '10'); // pregunta si son distintos tipos de dato o valor
